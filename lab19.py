# Assignment operator
# it stores the value of variable literal to the identifier
name = 'Aishwarya'

# unary operator-single value/single literal
age = +95
my_bank_balance = -100
print(age)
print(my_bank_balance)

# Not operator -Unary only applicable to booleans
is_Married = True
print(not is_Married)

# is operator--basically check for identity operator
a = 5
b = False
print(a is b)

my_list = [1, 2, 3, 4]
my_list2 = [1, 2, 3, 4]
print(my_list is my_list2)
